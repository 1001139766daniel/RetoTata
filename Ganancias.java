package Forma_2;

public class Ganancias {
	
	//Variable ganancia
	double G;
	
	//Constructor
	public Ganancias(double G) {
		this.G = G;
	}
	
	//Calculo de operaciones
	public double Enero() {
		double E = this.G;
		return E;
	}
	
	public int Febrero() {
		int  F = (int) (this.G /2);
		return F;
	}
	
	public int Marzo() {
		int  Mz = (int) (this.G *2);
		return Mz;
	}
	
}
     
	

	


 